package edu.utexas.ece.mpc.seap;

/**
 * SunSPOT data input and access methods
 * @author <a href="mailto:dstovall@dstovall.org">Drew Stovall</a>
 * @author <a href="mailto:smholloway@gmail.com">Seth Holloway</a>
 */
public class SunSPOT {

    private double x;
    private double y;
    private double z;

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getZ() {
        return z;
    }

    public void setZ(double z) {
        this.z = z;
    }
}